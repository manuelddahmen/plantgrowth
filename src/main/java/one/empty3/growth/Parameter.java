package one.empty3.growth;

import one.empty3.library.core.raytracer.tree.*;

import java.util.Arrays;

public class Parameter {
    private String name;
    private Double value;
    public LSystem lSystem;
    private String formula;

    public Parameter(String name, Double parameter) {

        this.value = parameter;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getValue() {
        return value;
    }

    public void setValue(Double value) {
        this.value = value;
    }

    public Double eval(int t, int recurse) {
        this.value = value;
        return getValue();
    }

    public void computeParams() {
    }

    public String getFormula() {
        return formula;
    }

    @Override
    public String toString() {
        String s = "";
        return "Parameter: " + getName() + " Value: " + getValue() + " Formula: " + getFormula();
    }
}
